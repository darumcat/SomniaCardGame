export default function App() {
  const [account, setAccount] = useState(null);
  const [provider, setProvider] = useState(null);
  const [signer, setSigner] = useState(null);
  const [nftContract, setNftContract] = useState(null);
  const [usdCardContract, setUsdCardContract] = useState(null);
  const [cardGameContract, setCardGameContract] = useState(null);
  const [error, setError] = useState(null);
  const [isLoading, setIsLoading] = useState(false);
  const [gameStatus, setGameStatus] = useState('not_started');

  // Инициализация контрактов
  useEffect(() => {
    if (signer) {
      setNftContract(new ethers.Contract(CONTRACTS.NFT, NFT_ABI, signer));
      setUsdCardContract(new ethers.Contract(CONTRACTS.USDCARD, USDCARD_ABI, signer));
      setCardGameContract(new ethers.Contract(CONTRACTS.CARDGAME, CARDGAME_ABI, signer));
    }
  }, [signer]);

  const connectWallet = async () => {
    if (!window.ethereum) {
      setError('Please install MetaMask!');
      return;
    }

    setIsLoading(true);
    try {
      const accounts = await window.ethereum.request({ 
        method: 'eth_requestAccounts' 
      });

      const chainId = await window.ethereum.request({ method: 'eth_chainId' });
      if (chainId !== SOMNIA_CONFIG.chainId) {
        await window.ethereum.request({
          method: 'wallet_addEthereumChain',
          params: [SOMNIA_CONFIG]
        });
      }

      const browserProvider = new ethers.BrowserProvider(window.ethereum);
      setProvider(browserProvider);
      setSigner(await browserProvider.getSigner());
      setAccount(accounts[0]);
    } catch (err) {
      setError(err.message);
    } finally {
      setIsLoading(false);
    }
  };

  // Функции минтинга
  const mintNFT = async () => {
    try {
      const tx = await nftContract.mint();
      await tx.wait();
      alert('NFT minted successfully!');
    } catch (err) {
      setError('NFT mint failed');
    }
  };

  const mintUSDCard = async (amount = 100) => {
    try {
      const tx = await usdCardContract.mint(amount);
      await tx.wait();
      alert(`${amount} USDCard tokens minted!`);
    } catch (err) {
      setError('USDCard mint failed');
    }
  };

  // Игровые функции
  const startGame = async (opponentAddress) => {
    try {
      const tx = await cardGameContract.startGame(opponentAddress, 0); // 0 - обычная игра
      await tx.wait();
      setGameStatus('started');
    } catch (err) {
      setError('Game start failed');
    }
  };

  return (
    <div className="App">
      <h1>Somnia Card Game</h1>
      
      {!account ? (
        <button 
          className="connect-button"
          onClick={connectWallet}
          disabled={isLoading}
        >
          {isLoading ? 'Connecting...' : 'Connect Wallet'}
        </button>
      ) : (
        <div className="game-container">
          <div className="wallet-info">
            <p>Connected: {account.slice(0, 6)}...{account.slice(-4)}</p>
            <p>Network: {SOMNIA_CONFIG.chainName}</p>
          </div>

          <div className="mint-section">
            <h3>Get Started</h3>
            <button onClick={mintNFT}>Mint Free NFT</button>
            <button onClick={() => mintUSDCard(100)}>Mint 100 USDCard</button>
          </div>

          <div className="game-section">
            <h3>Start Game</h3>
            <input 
              type="text" 
              placeholder="Opponent address" 
              id="opponentAddress"
            />
            <button onClick={() => 
              startGame(document.getElementById('opponentAddress').value)
            }>
              Play Durak
            </button>
          </div>

          {error && <div className="error">{error}</div>}
        </div>
      )}
    </div>
  );
}
